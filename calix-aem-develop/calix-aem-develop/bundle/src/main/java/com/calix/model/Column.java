package com.calix.model;

/**
 * The Class Column.
 */
public class Column {

	/** The cell value. */
	private String cellValue;

	/**
	 * Gets the cell value.
	 *
	 * @return the cell value
	 */
	public String getCellValue() {
		return cellValue;
	}

	/**
	 * Sets the cell value.
	 *
	 * @param cellValue the new cell value
	 */
	public void setCellValue(String cellValue) {
		this.cellValue = cellValue;
	}
	
	
}
